      <p><b>Passo 2.3:</b> Tramitar para a chefia (titular da unidade). A chefia deverá deferir ou não o pedido. Em caso positivo, tramitar o protocolo para a DTI;</p>
      <p><b>Passo 2.4:</b> Aguardar enquanto a DTI renova a chave ARES e entre em contato com o solicitante. Este procedimento tem uma duração máxima de 2 dias, após
      chegada do documento tramitado no Passo 2.3 junto aos analistas da DTI;</p>
      <p><b>Passo 2.5:</b> Ao término da renovação da CHAVE chegará um e-mail ao solicitante informando que sua renovação foi finalizada e contendo instruções
      de como proceder. Leia-o atentamente;</p>

